package com.deleidos.bm.server;

import java.io.BufferedReader;
import java.io.File;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.io.InputStream;
import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.SortedMap;
import java.util.TreeMap;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;
import java.util.concurrent.TimeUnit;

import javax.xml.bind.JAXBContext;
import javax.xml.bind.JAXBException;
import javax.xml.bind.Unmarshaller;

import messages.alerts.pojos.AlertSettings;

import org.apache.log4j.Logger;
import org.codehaus.jackson.map.ObjectMapper;
import org.codehaus.jackson.type.TypeReference;

import com.deleidos.bm.processor.AlertProcessor;
import com.deleidos.bm.processor.RestUrlProcessor;
import com.deleidos.bm.processor.RestUrlsInfo;
import com.deleidos.bm.processor.RestUrlsInfo.RestUrlInfo;
import com.deleidos.rtws.commons.config.UserDataProperties;
import com.deleidos.rtws.commons.jersey.config.JerseyClientConfig;
import com.sun.jersey.api.client.Client;

import dataStore.DataStore;

/**
 * Manages all the @UsageProcessor objects and the @DataStore where metrics are
 * stored. Also stores the @AlertSettings and users and manages the alerts using
 * an @AlertProcessor to monitor the metrics. Uses a thread pool to manage the
 * processors and handles startup and shutdown events.
 * 
 * @author devlinm
 * 
 */
public class CollectorServer {

	private static Logger logger = Logger.getLogger(CollectorServer.class);

	private final static String ALERTS_SAVE_FILE = "/mnt/rdafs/BusinessMetricsAlerts.json";
	private final static String ALERTS_USERS_SAVE_FILE = "/mnt/rdafs/BusinessMetricsAlertsUsers.json";

	private final int TERMINATION_WAIT_TIME = 90;

	private boolean endServerCalled;

	private ExecutorService pool;
	private List<RestUrlProcessor> runningTasks;

	private AlertProcessor alertProcessor;

	private static Client REST_CLIENT;

	private static volatile DataStore dataStore;

	private static volatile SortedMap<String, AlertSettings> alerts;
	private static volatile SortedMap<String, String> users;

	public CollectorServer() {
		runningTasks = Collections.synchronizedList(new ArrayList<RestUrlProcessor>());
		String restXml = UserDataProperties.isTmsSystem() ? "TMS-Rest-Urls.xml"
				: "Gateway-Rest-Urls.xml";

		logger.info("Loading rest commands from " + restXml);

		JAXBContext jaxbContext = null;
		try {
			jaxbContext = JAXBContext.newInstance(RestUrlsInfo.class);
			Unmarshaller jaxbUnmarshaller = jaxbContext.createUnmarshaller();
			InputStream is = getClass().getResourceAsStream("/" + restXml);
			RestUrlsInfo urls = (RestUrlsInfo) jaxbUnmarshaller.unmarshal(is);
			for (RestUrlInfo rest : urls.getRestUrls())
				runningTasks.add(new RestUrlProcessor(rest));

		} catch (JAXBException e) {
			logger.fatal("Error getting the rest urls!", e);
		}
	}

	public static synchronized Client getRestClient() {
		if (REST_CLIENT == null)
			REST_CLIENT = Client.create(JerseyClientConfig.getInstance().getInternalConfig(4000,
					4000));
		return REST_CLIENT;
	}

	/**
	 * Starts the CollectorServer, the @UsageProcessor, and an @AlertProcessor
	 */
	public void initialize() {
		endServerCalled = false;

		// + 1 for alert processor
		pool = Executors.newFixedThreadPool(runningTasks.size() + 1);

		for (RestUrlProcessor processor : runningTasks) {
			pool.execute(processor);
		}

		alertProcessor = new AlertProcessor();

		pool.execute(alertProcessor);

	}

	/**
	 * Closes all connections and stops the server from running. Allows the
	 * running threads time to shutdown before forcing them to stop. Calls the
	 * DataStore's save method. Also saves the alert settings and information
	 * about the users
	 */
	public void endServer() {
		if (!endServerCalled) {
			logger.info("[Server]: Ending the server...");
			endServerCalled = true;
			logger.info("[Server]: Saving the data store...");
			try {
				dataStore.saveDataStore();
			} catch (Exception e) {
				logger.error("[Server Error]: Error saving data store! Data may be lost!", e);
			}
			saveAlerts();
			logger.info("[Server]: Stopping the worker threads with a wait time of "
					+ TERMINATION_WAIT_TIME + " seconds");
			shutdownAndAwaitTermination();
		}
	}

	private void shutdownAndAwaitTermination() {
		try {
			pool.shutdown(); // Disable new tasks from being submitted
			for (int i = 0; i < runningTasks.size(); i++) {
				runningTasks.get(i).stop();
			}

			try {
				// Wait a while for existing tasks to terminate
				if (!pool.awaitTermination(TERMINATION_WAIT_TIME, TimeUnit.SECONDS)) {
					pool.shutdownNow(); // Cancel currently executing tasks
					// Wait a while for tasks to respond to being cancelled
					if (!pool.awaitTermination(TERMINATION_WAIT_TIME, TimeUnit.SECONDS))
						logger.error("[Server]: Pool did not terminate in the required time.");
				}
			} catch (InterruptedException ie) {
				// (Re-)Cancel if current thread also interrupted
				pool.shutdownNow();
				// Preserve interrupt status
				Thread.currentThread().interrupt();
			}
		} catch (Exception e) {
			logger.error(e);
		}
		logger.info("[Server]: Shutdown finished executing.");
	}

	/**
	 * Returns a reference to the data store that is storing all the metrics
	 * 
	 * @return the data store being used by this server
	 */
	public static DataStore getDataStore() {
		if (dataStore == null)
			dataStore = new InMemoryDataStore();
		return dataStore;
	}

	/**
	 * returns a SortedMap that maps an alert's name to an @AlertSettigns object
	 * 
	 * @return the alerts that the collector server contains
	 */
	public static SortedMap<String, AlertSettings> getAlerts() {
		if (alerts == null) {
			loadAlerts();
		}
		return alerts;
	}

	/**
	 * Returns a SortedMap that maps a user's name to that user's email address
	 * 
	 * @return the information about the users that the CollectorServer contains
	 */
	public static SortedMap<String, String> getUsers() {
		if (users == null) {
			loadAlerts();
		}
		return users;
	}

	/*
	 * Loads the alerts and users from the files above if any information is
	 * available
	 */
	private static void loadAlerts() {
		alerts = new TreeMap<String, AlertSettings>();
		users = new TreeMap<String, String>();
		try {
			File saveData = new File(ALERTS_SAVE_FILE);
			File usersSaveData = new File(ALERTS_USERS_SAVE_FILE);
			if (saveData.exists()) {
				logger.info("Loading saved alerts data from " + ALERTS_SAVE_FILE);
				BufferedReader reader;
				reader = new BufferedReader(new FileReader(saveData));
				ObjectMapper mapper = new ObjectMapper();
				String line = null;
				while ((line = reader.readLine()) != null) {
					AlertSettings settings = mapper.readValue(line, AlertSettings.class);
					alerts.put(settings.alertName, settings);
				}
				reader.close();
			} else {
				logger.info("No saved alerts data to load from " + ALERTS_SAVE_FILE);
			}

			if (usersSaveData.exists()) {
				logger.info("Loading saved users data from " + ALERTS_USERS_SAVE_FILE);
				ObjectMapper mapper = new ObjectMapper();
				users = mapper.readValue(usersSaveData,
						new TypeReference<SortedMap<String, String>>() {
						});
			} else {
				logger.info("No saved users data to load from " + ALERTS_USERS_SAVE_FILE);
			}

		} catch (Exception e) {
			logger.error("Error collecting save alert settings from " + ALERTS_SAVE_FILE, e);
		}
	}

	/*
	 * Saves the alerts and users to the specified files
	 */
	private static void saveAlerts() {
		logger.info("Saving AlertSettings in " + ALERTS_SAVE_FILE);
		try {
			FileWriter writer = new FileWriter(new File(ALERTS_SAVE_FILE));
			for (AlertSettings setting : alerts.values()) {
				writer.write(setting.toJSON() + "\n");
			}
			writer.flush();
			writer.close();
		} catch (IOException e) {
			logger.error("Error saving the alert settings!", e);
		}
		logger.info("Saving users in " + ALERTS_USERS_SAVE_FILE);
		try {
			ObjectMapper mapper = new ObjectMapper();
			mapper.writeValue(new File(ALERTS_USERS_SAVE_FILE), users);
		} catch (IOException e) {
			logger.error("Error saving the users settings!", e);
		}
	}
}
