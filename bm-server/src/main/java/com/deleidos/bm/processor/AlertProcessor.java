package com.deleidos.bm.processor;

import java.util.Arrays;
import java.util.Properties;

import javax.mail.Message;
import javax.mail.Session;
import javax.mail.Transport;
import javax.mail.internet.InternetAddress;
import javax.mail.internet.MimeMessage;

import messages.alerts.pojos.AlertSettings;

import org.apache.log4j.Logger;

import analyzing.RunningTimeRatio;
import analyzing.SystemUsageAnalyzer;

import com.deleidos.bm.server.CollectorServer;
import com.deleidos.rtws.commons.cloud.platform.aws.AwsConnectionFactory;
import com.deleidos.rtws.commons.cloud.platform.aws.AwsEmailConfiguration;
import com.deleidos.rtws.commons.cloud.platform.aws.AwsSimpleEmailSender;
import com.deleidos.rtws.commons.config.UserDataProperties;

/**
 * Monitors the metrics and alerts from the @CollectorServer to check in every
 * interval if any alerts have been triggered. If an alert is triggered, the @AlertSetting
 * object is changed to reflect it. An email is also dispatched to the users who
 * are attached to the alert.
 * 
 * @author devlinm
 * 
 */
public class AlertProcessor implements Runnable {

	private boolean running;
	private static long checkInterval = 60000;
	private Logger logger = Logger.getLogger(getClass());

	@Override
	public void run() {
		logger.info("AlertProcessor started on an interval of " + checkInterval);
		running = true;
		while (running) {
			long start = System.currentTimeMillis();

			for (AlertSettings settings : CollectorServer.getAlerts().values()) {
				String alertTriggerMessage = null;
				// make sure the alert is enabled and hasn't triggered yet.
				if (settings.enabled && !settings.hasTriggered) {
					// for each system attached to the alert
					for (String system : settings.systems) {

						// check the system's idle time
						if (settings.maxIdleTime > 0) {
							RunningTimeRatio ratio = SystemUsageAnalyzer.getRunningRatio(
									CollectorServer.getDataStore().getSystemUsage(system), 0,
									Long.MAX_VALUE);
							if (ratio.idleTime >= settings.maxIdleTime) {
								alertTriggerMessage = getIdleTimeTriggerMessage(settings, system,
										ratio);
							}
						}

						// check the system's performance
						if (settings.performanceRatioDelay > 0 && settings.minPerformanceRatio > 0) {
							if (settings.performanceRatioDelay > System.currentTimeMillis()
									- settings.createdAt) {
								RunningTimeRatio ratio = SystemUsageAnalyzer.getRunningRatio(
										CollectorServer.getDataStore().getSystemUsage(system), 0,
										Long.MAX_VALUE);
								if (ratio.runVsIdleTime() < settings.minPerformanceRatio) {
									alertTriggerMessage = getPerformanceRatioTriggerMessage(
											settings, system, ratio);
								}
							}
						}

						// check the system's estimated cost
						if (settings.maxCost > 0) {
							double systemCost = SystemUsageAnalyzer.getCostOfSystem(CollectorServer
									.getDataStore().getSystemUsage(system), 0, Long.MAX_VALUE);
							if (systemCost >= settings.maxCost) {
								alertTriggerMessage = getCostTriggerMessage(settings, system,
										systemCost);
							}
						}

						// check the system's rate of ingest
						if (settings.ingestRateDelay > 0 && settings.minIngestRate > 0) {
							if (settings.ingestRateDelay > System.currentTimeMillis()
									- settings.createdAt) {
								long recordsIngested = SystemUsageAnalyzer
										.getRecordsIngested(CollectorServer.getDataStore()
												.getSystemUsage(system));
								long uptime = SystemUsageAnalyzer.getRunningRatio(
										CollectorServer.getDataStore().getSystemUsage(system), 0,
										Long.MAX_VALUE).getTotalTime();
								double recordsPerSec = recordsIngested / (uptime / 1000.0);
								if (recordsPerSec < settings.minIngestRate) {
									alertTriggerMessage = getIngestRateTriggerMessage(settings,
											system, recordsPerSec);
								}
							}
						}

						// if the alertMessage isn't null, an alert was
						// triggered
						if (alertTriggerMessage != null) {
							triggerAlert(settings, alertTriggerMessage);
							// alert triggered so no need to check other systems
							break;
						}

					}
				}
			}

			long finish = System.currentTimeMillis();

			long duration = finish - start;
			long delay = checkInterval - duration;

			if (delay > 0) {
				try {
					Thread.sleep(delay);
				} catch (InterruptedException e) {
					logger.info("RestUrlProcessor interrupted");
					break;
				}
			}
		}
	}

	private String getIdleTimeTriggerMessage(AlertSettings settings, String system,
			RunningTimeRatio ratio) {
		String messageFormat = "Alert '%s' has been triggered because system '%s' has been idle for %.2f hours which is greater than the set idle time of %.2f.%n";
		return String.format(messageFormat, settings.alertName, system, ratio.idleTime
				/ (1000.0 * 60.0 * 60.0), settings.maxIdleTime / (1000.0 * 60.0 * 60.0));
	}

	private String getPerformanceRatioTriggerMessage(AlertSettings settings, String system,
			RunningTimeRatio ratio) {
		String messageFormat = "Alert '%s' has been triggered by system '%s' because its ratio of time spent running vs idle is currently at %.2f%% which is below the minimum ratio of %.2f%%.%n";
		return String.format(messageFormat, settings.alertName, system, ratio.runVsIdleTime(),
				settings.minPerformanceRatio);
	}

	private String getCostTriggerMessage(AlertSettings settings, String system, double cost) {
		String messageFormat = "Alert '%s' has been triggered by system '%s' because the estimated cost of the system is currently $%.2f which is greater than the alert's cost of $%.2f.%n";
		return String.format(messageFormat, settings.alertName, system, cost, settings.maxCost);
	}

	private String getIngestRateTriggerMessage(AlertSettings settings, String system,
			double recordsPerSec) {
		String messageFormat = "Alert '%s' has been triggered by system '%s' because its records per second is currently at %.2f which is below the minimum amount of %.2f.%n";
		return String.format(messageFormat, settings.alertName, system, recordsPerSec,
				settings.minIngestRate);
	}

	/*
	 * Marks the @AlertSettings object as triggered and sends an email to the
	 * users using AWS Simple Email if available, and otherwise uses JavaMail.
	 */
	private void triggerAlert(AlertSettings settings, String alertMessage) {
		String[] to = settings.users.values().toArray(new String[settings.users.size()]);
		String from = "digitaledge-bm-alerts@deleidos.com";
		String host = "localhost";
		String subject = "DigitalEdge: Alert '" + settings.alertName + "' Triggered";
		String msgBody = "Dear DigitalEdge User,\n";
		msgBody += "An alert you have set in the DigitalEdge Business Metrics has been triggered:\n";
		msgBody += alertMessage;
		msgBody += "To manage your alerts, go to the Alert Settings page in the Business Metrics Console";
		Properties properties = System.getProperties();
		properties.setProperty("mail.smtp.host", host);

		try {
			// try to email through AWS
			AwsEmailConfiguration emailConfig = new AwsEmailConfiguration();
			AwsConnectionFactory factory = new AwsConnectionFactory(UserDataProperties
					.getInstance().getString(UserDataProperties.RTWS_ACCESS_KEY),
					UserDataProperties.getInstance().getString(UserDataProperties.RTWS_SECRET_KEY));
			emailConfig.setAwsConnectionFactory(factory);
			AwsSimpleEmailSender awsEmail = new AwsSimpleEmailSender();
			awsEmail.configure(emailConfig);
			if (awsEmail.sendEmail(from, to, subject, msgBody)) {
				logger.info("Sent alert email successfuly to " + to + " using AWS");
			} else {
				// Email failed to send, so AWS Simple Email might not be
				// available, so try to send through JavaMail
				logger.error("Failed to send alert email using AWS. Trying with JavaMail...");
				for (String email : to) {
					Session session = Session.getDefaultInstance(properties);
					MimeMessage message = new MimeMessage(session);
					message.setFrom(new InternetAddress(from));
					message.addRecipient(Message.RecipientType.TO, new InternetAddress(email));
					message.setSubject(subject);
					message.setText(msgBody);
					Transport.send(message);
				}
				logger.info("Sent alert email successfuly to " + Arrays.toString(to)
						+ " using JavaMail");
			}
		} catch (Exception e) {
			logger.error("Error sending the alert email to '" + Arrays.toString(to) + "'.", e);
		}
		// Mark the alert as triggered
		settings.triggeredAt = System.currentTimeMillis();
		settings.hasTriggered = true;
		settings.enabled = false;
		settings.alertSeen = false;
	}

	/**
	 * Stops the @AlertProcessor from executing any more
	 */
	public void stop() {
		running = false;
	}

}
