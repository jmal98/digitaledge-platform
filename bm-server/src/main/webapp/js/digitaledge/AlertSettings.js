	var activeCode = "<strong>Select Users to Alert (" + usersArr.length
			+ " Avaliable):</strong><br/>";

	var selectAllActive = "<div class=\"col-md-4\" name='allUsers'>";
	selectAllActive += "<div class='systemTextLabel'><strong>Select All</strong></div>";
	selectAllActive += "<div class='slideTwo'> <input type='checkbox' value='None' class='slideTwoLabel' ";
	selectAllActive += "id='allUsersSelect' /> <label for='slideTwo'></label></div></div>";
	activeCode += selectAllActive;

	for ( var index in usersArr) {
		var user = usersArr[index].replace(/\s/g, '');
		activeCode += "<div class=\"col-md-4\" name='userSlider' onclick=\"$('#"
				+ user;
		activeCode += "Slider').click(); \"> <div class='systemTextLabel'><strong>"
				+ usersArr[index] + "</strong></div>";
		activeCode += "<div class='slideTwo'> <input type='checkbox' value='None' class='slideTwoLabel'";
		activeCode += "id='" + user + "Slider' data-user='" + usersArr[index]
				+ "' /> <label for='slideTwo'></label></div></div>";
	}

