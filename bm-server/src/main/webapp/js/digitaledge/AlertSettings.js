function getSelectedSystems() {
	var systemNames = [];
	for ( var systemName in SelectedSystemAlerts) {
		if (SelectedSystemAlerts[systemName])
			systemNames.push(systemName);
	}
	return systemNames;
}

function getSelectedUsers() {
	var users = {};
	for ( var user in AlertsUsers) {
		if (SelectedUsers[user])
			users[user] = AlertsUsers[user];
	}
	return users;
}