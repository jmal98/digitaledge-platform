$("#saveAlert").click(
		function() {
			if (!validAlertName($("#alertNameInput").val())
					|| !validNum($("#idleTimeInput").val())
					|| !validPerc($("#performanceInput").val())
					|| !validNum($("#performanceTimerInput").val())
					|| !validNum($("#costInput").val())
					|| !validNum($("#ingestRateInput").val())
					|| !validNum($("#ingestRateTimerInput").val())) {
				$("#saveAlert").popover('enable');
				$("#saveAlert").popover('show');
				if (!validAlertName($("#alertNameInput").val())) {
					showErrors("#alertNameInput", "#alertNameGroup");
				} else {
					hideErrors("#alertNameInput", "#alertNameGroup");
				}
			} else {
				$("#saveAlert").popover('hide');
				$("#saveAlert").popover('disable');
				$('#createModal').modal('hide');
				sendCreateAlert();
			}
		});

function validAlertName(name) {
	var patt = new RegExp("^([A-z]+|[0-9]+|-+|_+|(\\s)+)*$");
	return patt.test(name) && name.length != 0;
}

function validNum(time) {
	if (time.length == 0)
		return true;
	var t = time * 1;
	return t >= 1;
}

function validPerc(perc) {
	if (perc.length == 0)
		return true;
	var t = perc * 1;
	return t >= 1 && t < 100;
}

function loadSystemSelectionList(json) {
	var systemNameArr = [];
	for ( var i in json['systems']) {
		systemNameArr.push(json['systems'][i]);
	}

	systemNameArr.sort();

	var activeCode = "<strong>Select Systems to Monitor ("
			+ systemNameArr.length + " Avaliable):</strong><br/>";

	var selectAllActive = "<div class=\"col-md-4\" name='activeSystemAll' >";
	selectAllActive += "<div class='systemTextLabel'><strong>Select All</strong></div>";
	selectAllActive += "<div class='slideTwo'> <input type='checkbox' value='None' class='slideTwoLabel' ";
	selectAllActive += "id='selectAllActive' /> <label for='slideTwo'></label></div></div>";
	activeCode += selectAllActive;

	for ( var index in systemNameArr) {
		var system = systemNameArr[index].split(".")[0];
		activeCode += "<div class=\"col-md-4\" name='activeSystem' onclick=\"$('#"
				+ system;
		activeCode += "Slider').click(); \"> <div class='systemTextLabel'><strong>"
				+ system + "</strong></div>";
		activeCode += "<div class='slideTwo'> <input type='checkbox' value='None' class='slideTwoLabel'";
		activeCode += "id='" + system + "Slider' name='" + systemNameArr[index]
				+ "' /> <label for='slideTwo'></label></div></div>";
	}

	$(alertSystemList).html(activeCode);

	$(".slideTwoLabel").click(function(e) {
		if ($(this).is(':checked') && $(this).attr("name")) {
			SelectedSystemAlerts[$(this).attr("name")] = true;
		} else {
			SelectedSystemAlerts[$(this).attr("name")] = false;
		}
		e.stopPropagation();
	});

	$("div[name='activeSystemAll']").click(function() {
		$("#selectAllActive").click();
		if ($("#selectAllActive").is(':checked')) {
			$(".slideTwoLabel").each(function(i) {
				if (!$(this).is(':checked')) {
					$(this).click();
				}
			});
		} else {
			$(".slideTwoLabel").each(function(i) {
				if ($(this).is(':checked')) {
					$(this).click();
				}

			});
		}
	});
}

