/*
 * {users : {<name>:<email>}}
 */
function handleGetUsers(json) {
	$(usersTable).DataTable().clear();
	for ( var user in json['users']) {
		var data = [];
		data[0] = user;
		data[1] = json['users'][user];
		data[2] = "<button style='width:0%; height:0%; padding-left:18%; visibility:hidden'></button><button class='btn btn-default btn-xs' style='width:50%' name='editUser' data-user-name='"
				+ user
				+ "'><i class='fa fa-pencil fa-fw'></i>&nbsp;Edit</button>";
		data[3] = "<button style='width:0%; height:0%; padding-left:18%; visibility:hidden'></button><button class='btn btn-default btn-xs' style='width:50%' name='deleteUser' data-user-name='"
				+ user
				+ "'><i class='fa fa-trash-o fa-fw'></i>&nbsp;Remove</button>";
		$(usersTable).DataTable().row.add(data);
	}
	AlertsUsers = json['users'];
	$(usersTable).DataTable().draw();
	createUserToggles();

	$("button[name='editUser']").click(function() {
		var oldUser = $(this).attr("data-user-name");
		deleteUser(null, oldUser);
		var user = $(this).attr("data-user-name");
		$("#userNameInput").val(user);
		$("#userEmailInput").val(AlertsUsers[user]);
		$('#createUserModal').modal('show');
	});
	$("button[name='deleteUser']").click(function() {
		var user = $(this).attr("data-user-name");
		deleteUser(userChangedCallback, user);
	});
}

function sendCreateAlert() {
	var alertName = $("#alertNameInput").val();
	var maxIdleTime = $("#idleTimeInput").val();
	if (maxIdleTime.length == 0)
		maxIdleTime = -1;
	else
		maxIdleTime = maxIdleTime * 60 * 60 * 1000;
	var performanceRatioDelay = $("#performanceTimerInput").val();
	if (performanceRatioDelay.length == 0)
		performanceRatioDelay = -1;
	else
		performanceRatioDelay = performanceRatioDelay * 60 * 60 * 1000;
	var ingestRateDelay = $("#ingestRateTimerInput").val();
	if (ingestRateDelay.length == 0)
		ingestRateDelay = -1;
	else
		ingestRateDelay = ingestRateDelay * 60 * 60 * 1000;
	var minPerformanceRatio = $("#performanceInput").val();
	if (minPerformanceRatio.length == 0)
		minPerformanceRatio = -1;
	var maxCost = $("#costInput").val();
	if (maxCost.length == 0)
		maxCost = -1;
	var minIngestRate = $("#ingestRateInput").val();
	if (minIngestRate.length == 0)
		minIngestRate = -1;
	var systems = getSelectedSystems();
	createAlert(alertChangedCallback, alertName, maxIdleTime,
			performanceRatioDelay, ingestRateDelay, minPerformanceRatio,
			maxCost, minIngestRate, systems, getSelectedUsers());
}

