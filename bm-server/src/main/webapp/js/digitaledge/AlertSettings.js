function showErrors(input, group) {
	$(group).addClass("has-error");
	$(input).popover('enable');
	$(input).popover('show');
}

function hideErrors(input, group) {
	$(group).removeClass("has-error");
	$(input).popover('hide');
	$(input).popover('disable');
}

$("#alertNameInput").focusout(function() {
	if (!validAlertName($("#alertNameInput").val())) {
		showErrors("#alertNameInput", "#alertNameGroup");
	} else {
		hideErrors("#alertNameInput", "#alertNameGroup");
	}
});

